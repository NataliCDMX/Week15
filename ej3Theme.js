function show(){
    switch (document.getElementById('theme').value){
      case 'dark' :   document.body.style.backgroundColor = "dimgray"; break;
  case 'light' :   document.body.style.backgroundColor = "snow"; break;
  case 'coloredLight' : document.body.style.backgroundColor = "lightblue"; break;
  case 'coloredDark' :    document.body.style.backgroundColor = "indigo"; break;
    }
  }




